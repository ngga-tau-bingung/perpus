class UsersController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('admin.user.index',
        [
            "users" => User::orderBy('id', 'DESC')->paginate(5),
        ]);
    }

